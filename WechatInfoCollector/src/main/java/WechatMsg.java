public class WechatMsg {

    private String msgCongtent;

    private String msgSenderName;

    private String msgSendDate;

    public String getMsgCongtent() {
        return msgCongtent;
    }

    public void setMsgCongtent(String msgCongtent) {
        this.msgCongtent = msgCongtent;
    }

    public String getMsgSenderName() {
        return msgSenderName;
    }

    public void setMsgSenderName(String msgSenderName) {
        this.msgSenderName = msgSenderName;
    }

    public String getMsgSendDate() {
        return msgSendDate;
    }

    public void setMsgSendDate(String msgSendDate) {
        this.msgSendDate = msgSendDate;
    }
}
